microsecond = 1000 * nanosecond
millisecond = 1000 * microsecond
second      = 1000 * millisecond
minute      = 60 * second
hour        = 60 * minute
day         = 24 * hour
week        = 7 * day
month       = 30 * day
year        = 365 * day
decade      = 10 * year
century     = 100 * year

